async def generate_speaker_audio(text: str, voice: str, output_path: Path, max_retries: int = 5) -> None:
    """生成单句 TTS 音频并保存为 wav，带重试。"""
    from pydub import AudioSegment

    mp3_path = output_path.with_suffix(".mp3")
    for attempt in range(max_retries):
        try:
            communicate = edge_tts.Communicate(text, voice)
            await communicate.save(str(mp3_path))
            break
        except Exception as e:
            if attempt < max_retries - 1:
                wait = 2 ** attempt
                print(f"    重试 ({attempt+1}/{max_retries}), 等待 {wait}s: {e!r}")
                await asyncio.sleep(wait)
            else:
                raise

    audio = AudioSegment.from_mp3(str(mp3_path))
    audio = audio.set_frame_rate(16000).set_channels(1)  # 16kHz mono for ASR
    audio.export(str(output_path), format="wav")
    mp3_path.unlink()


async def generate_dialog_audio(
    dialog: list[tuple[str, str]],
    output_path: Path,
    pause_ms: int = 800,
) -> None:
    """将整段对话合成为一个音频文件。"""
    from pydub import AudioSegment

    combined = AudioSegment.silent(duration=500)  # 开头静音

    for i, (speaker, text) in enumerate(dialog):
        voice = SPEAKERS[speaker]
        tmp_path = AUDIO_DIR / f"_tmp_{i}.wav"

        print(f"  [{i+1}/{len(dialog)}] {speaker}: {text[:30]}...")
        await generate_speaker_audio(text, voice, tmp_path)

        segment = AudioSegment.from_wav(str(tmp_path))
        combined += segment
        combined += AudioSegment.silent(duration=pause_ms)  # 句间停顿

        tmp_path.unlink()

    combined.export(str(output_path), format="wav")
    duration = len(combined) / 1000
    print(f"  -> {output_path.name}: {duration/60:.1f} minutes, {len(dialog)} utterances")


